document.addEventListener('DOMContentLoaded', () => {
    // قائمة العناوين المحددة
    const fixedTitles = [
        'حرب غزة مستمرة ليومها 692',
        'تصنيف دولي يؤكد وقوع المجاعة في غزة ويهدد بانتشارها إلى مناطق أخرى.',
        'وزارة الصحة الفلسطينية تعلن عن ارتفاع عدد الشهداء إلى أكثر من 63 ألفًا.',
        'مقتل العشرات في غارات إسرائيلية على منازل وخيام النازحين في غزة وخان يونس.',
        'الأمم المتحدة ومنظمات إغاثية تحذر من تفاقم الأزمة الإنسانية وتدهور الأوضاع الصحية في القطاع.',
        'تركيا تقطع علاقاتها التجارية مع إسرائيل بشكل كامل.',
        'مسؤولون أميركيون ودوليون يبحثون خطة ما بعد الحرب في غزة دون التوصل لاتفاق.'
    ];

    const titleElements = document.querySelectorAll('.news-title');
    
    titleElements.forEach((element, index) => {
        if (fixedTitles[index]) {
            element.textContent = fixedTitles[index];
        } else {
            element.textContent = 'متابعة لأحدث التطورات';
        }
    });
});